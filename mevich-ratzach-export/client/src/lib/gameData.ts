export function uid() {
  return Math.random().toString(36).slice(2) + Date.now().toString(36);
}

export function makeCode() {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let code = "";
  for (let i = 0; i < 4; i++) code += alphabet[Math.floor(Math.random() * alphabet.length)];
  return code;
}

export function getGuestId() {
  if (typeof window === "undefined") return uid();
  const key = "mevich_client_id";
  let id = localStorage.getItem(key);
  if (!id) { id = uid(); localStorage.setItem(key, id); }
  return id;
}

export function shuffle<T>(arr: T[]) {
  return [...arr].sort(() => Math.random() - 0.5);
}

export function blankPlayer(id: string, name: string, isHost = false): Player {
  return { id, name, isHost, score: 0, hand: [], wins: 0, chaos: 0, votes: 0, reputation: [] };
}

export function allQuestions(packIds: string[]) {
  const ids = packIds.filter((id) => Boolean((PACKS as any)[id]));
  return ids.flatMap((id) => [...((PACKS as any)[id].questions)]);
}

export function allAnswers(packIds: string[]) {
  const ids = packIds.filter((id) => Boolean((PACKS as any)[id]));
  return ids.flatMap((id) => [...((PACKS as any)[id].answers)]);
}

export function titleForPlayer(p: Player) {
  if (p.score >= 5) return "💀 לא להזמין לחתונות";
  if (p.score >= 3) return "🚩 דגל אדום מהלך";
  if (p.wins >= 2) return "🔥 מסוכן לציבור";
  return "😶 עדיין נורמלי מדי";
}
